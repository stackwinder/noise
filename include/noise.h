double fade(double t) { return t * t * t * (t * (t * 6 - 15) + 10); }
double lerp(double t, double a, double b) { return a + t * (b - a); }

double grad(int hash, double x, double y, double z) {
	const int h = hash & 0xf;
	const double u = h < 8 ? x : y, v = h < 4 ? y : h == 12 || h == 14 ? x : z;
	return ((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v);
}

double noise(double x, double y, double z) {
	const double xf = floor(x), yf = floor(y), zf = floor(z);
	const int i = (int)xf & 0xff, j = (int)yf & 0xff, k = (int)zf & 0xff;
	x -= xf; y -= yf; z -= zf;
	const double u = fade(x), v = fade(y), w = fade(z);
	const int a = p[i    ] + j, aa = p[a] + k, ab = p[a + 1] + k,
	          b = p[i + 1] + j, ba = p[b] + k, bb = p[b + 1] + k;
	return lerp(w, lerp(v, lerp(u, grad(p[aa    ], x    , y    , z    ),
	                               grad(p[ba    ], x - 1, y    , z    )),
	                       lerp(u, grad(p[ab    ], x    , y - 1, z    ),
	                               grad(p[bb    ], x - 1, y - 1, z    ))),
	               lerp(v, lerp(u, grad(p[aa + 1], x    , y    , z - 1),
	                               grad(p[ba + 1], x - 1, y    , z - 1)),
	                       lerp(u, grad(p[ab + 1], x    , y - 1, z - 1),
	                               grad(p[bb + 1], x - 1, y - 1, z - 1))));
}
